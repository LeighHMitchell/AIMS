// Get all currencies with pinned ones first
export function getAllCurrenciesWithPinned(): Currency[] {
  const pinnedCodes = new Set(PINNED_CURRENCIES.map(c => c.code));
  const otherCurrencies = ALL_CURRENCIES.filter(c => !pinnedCodes.has(c.code));
  return [...PINNED_CURRENCIES, ...otherCurrencies];
}

// Get currency by code
export function getCurrencyByCode(code: string): Currency | undefined {
  return ALL_CURRENCIES.find(c => c.code === code) || 
         PINNED_CURRENCIES.find(c => c.code === code);
}

// Validate currency code
export function isValidCurrencyCode(code: string): boolean {
  return /^[A-Z]{3}$/.test(code) && 
         (ALL_CURRENCIES.some(c => c.code === code) || 
          PINNED_CURRENCIES.some(c => c.code === code));
} 